let currentLang = 'ja';

export function setLang(lang) {
  currentLang = LANGS.includes(lang) ? lang : 'ja';
}

export function getLang() {
  return currentLang;
}

// t('key', { name: 'value' }) -> translated string with {name} placeholders replaced.
export function t(key, vars) {
  const str = (dict[currentLang] && dict[currentLang][key]) || dict.ja[key] || key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, name) => (vars[name] !== undefined ? vars[name] : `{${name}}`));
}

// Apply translations to all elements with data-i18n / data-i18n-placeholder / data-i18n-title.
export function applyStaticTranslations(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.documentElement.lang = currentLang;
}
